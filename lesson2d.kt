fun main () {

    val marks = -10

    if (marks < 0){
        println("Invalid Marks")
    }
    else if (marks < = 20){
        println("You failed")
    }
    else if (marks < = 40){
        println("Below Avarage")
    }
    else if (marks < = 60){
        println("Average")
    }
    else if (marks < = 80){
        println("Above average")
    }
    else if (marks < = 100){
        println("You passed")
    }
    else{
        println("Invalid Marks")
    }


}